    "   - INPUT FIELD/TEXTBOX (editable): Text entry box, command prompt, search field, form input. Signs: cursor visible, focus outline, blinking caret, field is highlighted/active.\n"
    "   - OUTPUT AREA (non-editable): Terminal output, log pane, status bar, results display, code editor (when not being edited). Signs: no cursor, gray/disabled appearance, scrollable list.\n"
    "   - DIALOG/PROMPT (system-generated): Message box, alert, question dialog, system notification. Signs: appears in center/overlay, has buttons, appears without user interaction.\n"
    "2. **Transcribe text EXACTLY**: Read the text literally and precisely.\n"
    "3. **Parse instruction text** (only if text is instruction language):\n"
