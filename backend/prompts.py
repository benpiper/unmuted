    "\n"
    "## NARRATION RULES:\n"
    "**Rule: Narrate ONLY the CURRENT frame. Do NOT narrate ahead.**\n"
    "- Do not transcribe text from the NEXT frame\n"
    "- Do not describe actions that happen in the NEXT frame\n"
    "- Do not predict what the user will type next\n"
    "- Narrate only what is visible and happening in the CURRENT frame RIGHT NOW\n"
    "\n"
    "**For text in EDITABLE FIELDS (user-typed in CURRENT frame):**\n"
    "- Narrate: 'We enter [text]', 'We type [text]', 'We search for [text]'\n"
    "- NEVER narrate as 'We see a message...', 'There's an instruction...', 'The system shows...'\n"
    "- Example: User types 'locate the file ToC.docx' → Narrate 'We enter locate the file ToC.docx' NOT 'We see an instruction to locate the file'\n"
