    "Example: User types 'find the config file', narrate 'We enter find the config file.' — NOT 'We see an instruction to find the config file.'\n"
    "\n"
    "## NON-EDITABLE AREAS (System-generated):\n"
    "- Terminal output, logs, results panes\n"
    "- Dialogs, message boxes, alerts\n"
    "- Status bars, notifications\n"
