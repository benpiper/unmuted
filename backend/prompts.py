    "Your objective is to generate a high-level holistic 'Story Plan' outlining the major phases of the technical task being performed. "
    "\n"
    "## ANALYSIS APPROACH:\n"
    "For each keyframe in sequence:\n"
    "1. **READ THE TEXT**: Transcribe any visible instructions, prompts, or output on screen. Read literally.\n"
    "2. **PARSE THE ACTIVITY**: What is the user or system doing? Break it down:\n"
    "   - ACTION/PHASE: What major objective is being pursued? (Setup, Configuration, Testing, Deployment, etc.)\n"
    "   - CONTEXT: What tools, files, or systems are involved?\n"
    "   - PROGRESSION: Is this a continuation of the previous phase, or a new distinct phase?\n"
    "3. **CLASSIFY THE PHASE**: Group keyframes into distinct logical phases based on objectives, not granular actions.\n"
    "\n"
    "## PHASE IDENTIFICATION RULES:\n"
    "- A new PHASE begins when the PRIMARY OBJECTIVE changes (e.g., from 'setting up environment' to 'writing code' to 'testing')\n"
    "- Do NOT create separate phases for minor steps within a single objective\n"
    "- Combine steps that serve the same high-level goal into one phase\n"
    "- Each phase should represent a distinct architectural or logical milestone\n"
    "\n"
    "## OUTPUT REQUIREMENTS:\n"
    "Identify the broad objectives of each phase (e.g., 'Configuring the network layer', 'Installing dependencies', 'Verifying the deployment'). "
    "Be PRECISE about what each phase accomplishes. Do NOT use vague descriptions like 'Doing setup'—state WHAT is being set up and WHY it matters to the overall flow. "
    "Do NOT include granular details like specific CLI commands, exact URLs, or individual button clicks—focus on the INTENT and SCOPE of each phase. "
    "\n"
    "Return a JSON object with a single key 'plan', which is an array of strings, each describing a distinct high-level phase in chronological order. "
    "Each description should be clear enough that someone unfamiliar with the recording would understand the purpose of that phase."
