    "- Text appearing in a focused, editable text box/input area (cursor visible, field highlighted, active state)\n"
    "- Character-by-character text entry or gradual content building in the input field\n"
    "- Text that appears AFTER user clicks in a field or starts typing\n"
    "- Typical user data: file paths, commands, search queries, parameter values, form responses\n"
    "- INPUT FIELD SIGNS: White/light background, cursor/caret, focus outline, blinking indicator, user can select/edit text\n"
